    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextField1.getText().isEmpty()  ){
            JOptionPane.showMessageDialog(this,"Debe rellenar el campos");
        }else{
            int index = principal.buscarDocente(jTextField1.getText());
            if(index>=0){
                int confirma = JOptionPane.showConfirmDialog(this, "Confirmar");
                if(JOptionPane.OK_OPTION == confirma){
                    principal.listaDocente.remove(index);
                    JOptionPane.showMessageDialog(this,"Usuario Eliminado");
                    this.setVisible(false);
                    principal.setVisible(true);
                    principal.actualizarDatosDocente();
                }else{
                  JOptionPane.showMessageDialog(this,"Usuario no eliminado");
                }
            }else{
                JOptionPane.showMessageDialog(this,"Usuario no encontrado");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

