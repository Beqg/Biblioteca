    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Debe rellenar el campos");
            
        }else{
  
            int index = principal.buscarDocente(jTextField2.getText());
            int index2 = principal.buscarLibro(jTextField1.getText());
            if(index>=0 && index2 >=0){
                
                int confirma = JOptionPane.showConfirmDialog(this, "Confirmar");
                if(JOptionPane.OK_OPTION == confirma){
                    JOptionPane.showMessageDialog(this,"Devolución Existosa");
                    this.setVisible(false);

                }else{
                  JOptionPane.showMessageDialog(this,null);
                }
            }else{
                JOptionPane.showMessageDialog(this,"El usuario no puede efectuar una devolución");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

