    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextField1.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this,"Debe rellenar el campo");
        }else{
            int index = principal.buscarLibro(jTextField1.getText());
            if(index>=0){
                int confirma = JOptionPane.showConfirmDialog(this, "Confirmar");
                if(JOptionPane.OK_OPTION == confirma){
                    principal.listaLibro.remove(index);
                    JOptionPane.showMessageDialog(this,"Libro eliminado.");
                    this.setVisible(false);
                    principal.setVisible(true);
                    principal.actualizarDatosLibro();
                }else{
                    JOptionPane.showMessageDialog(this,"Libro no eliminado.");
                }
            }else{
                JOptionPane.showMessageDialog(this,"Libro no encontrado.");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

