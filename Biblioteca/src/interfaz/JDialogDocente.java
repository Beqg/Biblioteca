    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField4.getText().isEmpty() || jTextField6.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Debe rellenar todos los campos");
        }else{
            String selected_text = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
            String selected_text2 = jComboBox2.getItemAt(jComboBox2.getSelectedIndex());
            Docente doc = new Docente(jTextField1.getText(),jTextField2.getText(),selected_text,jTextField4.getText(),
                "",jTextField6.getText(),selected_text2);
            this.setVisible(false);          
            principal.setVisible(true);
            principal.agregarRegistroDocente(doc);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

