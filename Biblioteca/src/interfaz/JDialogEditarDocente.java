    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     if(jTextField1.getText().isEmpty() ||nombre.getText().isEmpty()|| rut.getText().isEmpty()||
              carrera.getText().isEmpty()||profesion.getText().isEmpty()  ){
            JOptionPane.showMessageDialog(this,"Debe rellenar los campos");
        }else{
            int index = principal.buscarDocente(jTextField1.getText());
            if(index>=0){
                int confirma = JOptionPane.showConfirmDialog(this, "Confirmar");
                if(JOptionPane.OK_OPTION == confirma){     
                    String genero = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
                    String grado = jComboBox2.getItemAt(jComboBox2.getSelectedIndex());
                    Docente d = new Docente(nombre.getText(),rut.getText(),genero,carrera.getText(),"",profesion.getText(),grado);
                    principal.listaDocente.set(index, d);
                    JOptionPane.showMessageDialog(this,"Usuario modificado.");
                    this.setVisible(false);
                    principal.setVisible(true);
                    principal.actualizarDatosDocente();
                }else{
                  JOptionPane.showMessageDialog(this,"Usuario no modificado.");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

